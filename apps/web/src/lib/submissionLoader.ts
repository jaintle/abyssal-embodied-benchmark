/**
 * submissionLoader.ts — Per-submission artifact loading (Phase C)
 *
 * Loads per-submission metadata, summary, and replay files from the
 * published public data store at /public/data/submissions/<id>/.
 *
 * Published artifact layout (from Phase B publish_submission.py):
 *
 *   /data/submissions/<id>/
 *     metadata.json       — SubmissionMetadata (full author / algorithm info)
 *     summary.json        — SubmissionSummary (per-preset metrics)
 *     replays/
 *       clear/            — episode_0001.jsonl, episode_0002.jsonl, …
 *       heavy/
 *
 * All loading gracefully returns LoadResult so callers can show error states.
 */

import {
  safeValidateSubmissionMetadata,
  type SubmissionMetadata,
} from "@abyssal/replay-schema";
import { loadJsonlReplay, type LoadResult } from "./benchmarkLoader";
import type { ReplayFile } from "@abyssal/replay-schema";
import { DATA_ROOT } from "./leaderboardLoader";

// ─── Domain types ─────────────────────────────────────────────────────────────

/** Metrics for one degradation preset (from summary.json). */
export interface PresetMetrics {
  success_rate: number;
  collision_rate: number;
  timeout_rate: number;
  mean_reward: number;
  mean_final_dist: number;
  mean_steps: number;
  n_episodes: number;
}

/**
 * Per-submission summary bundle (summary.json).
 *
 * Generated by publish_submission.py from the verification manifest's
 * preset_metrics section.
 */
export interface SubmissionSummary {
  submission_id: string;
  agent_id: string;
  benchmark_version: string;
  world_seed: number;
  /** Total episodes used in the official evaluation. */
  n_episodes: number;
  /** Per-preset aggregate metrics keyed by preset name (e.g. "clear", "heavy"). */
  presets: Record<string, PresetMetrics>;
}

// ─── Loaders ─────────────────────────────────────────────────────────────────

/**
 * Load and validate metadata.json for a submission.
 *
 * Uses the metadata_path from the leaderboard entry (relative to DATA_ROOT).
 */
export async function loadSubmissionMetadata(
  metadataPath: string
): Promise<LoadResult<SubmissionMetadata>> {
  const url = `${DATA_ROOT}/${metadataPath}`;
  let res: Response;
  try {
    res = await fetch(url);
  } catch (e) {
    return { success: false, error: `Network error fetching metadata: ${String(e)}` };
  }
  if (!res.ok) {
    return { success: false, error: `Metadata not found (HTTP ${res.status}): ${url}` };
  }
  let raw: unknown;
  try {
    raw = await res.json();
  } catch {
    return { success: false, error: "metadata.json is not valid JSON." };
  }
  const result = safeValidateSubmissionMetadata(raw);
  if (!result.success) {
    return { success: false, error: "metadata.json failed schema validation." };
  }
  return { success: true, data: result.data };
}

/**
 * Load summary.json for a submission.
 *
 * Uses the summary_path from the leaderboard entry (relative to DATA_ROOT).
 */
export async function loadSubmissionSummary(
  summaryPath: string
): Promise<LoadResult<SubmissionSummary>> {
  const url = `${DATA_ROOT}/${summaryPath}`;
  let res: Response;
  try {
    res = await fetch(url);
  } catch (e) {
    return { success: false, error: String(e) };
  }
  if (!res.ok) {
    return { success: false, error: `HTTP ${res.status}: ${url}` };
  }
  try {
    const data = (await res.json()) as SubmissionSummary;
    return { success: true, data };
  } catch {
    return { success: false, error: "summary.json is not valid JSON." };
  }
}

/**
 * Load a single episode replay for a submission.
 *
 * @param replayBasePath  replay_path from leaderboard entry  (e.g. "submissions/ppo-v1/replays/")
 * @param preset          "clear" | "heavy" (or any named preset)
 * @param episodeIdx      1-based episode index
 */
export async function loadSubmissionReplay(
  replayBasePath: string,
  preset: string,
  episodeIdx: number
): Promise<LoadResult<ReplayFile>> {
  const idx = String(episodeIdx).padStart(4, "0");
  // Ensure no double-slash between base path and episode filename
  const base = replayBasePath.endsWith("/") ? replayBasePath : replayBasePath + "/";
  const url = `${DATA_ROOT}/${base}${preset}/episode_${idx}.jsonl`;
  const result = await loadJsonlReplay(url);
  if (!result.success) {
    return { success: false, error: String(result.error) };
  }
  return { success: true, data: result.data };
}

/**
 * Return the list of 1-based episode indices expected for a submission.
 *
 * Capped at 50 for the UI selector even if n_episodes is larger.
 */
export function availableEpisodeIndices(nEpisodes: number): number[] {
  const count = Math.min(Math.max(nEpisodes, 1), 50);
  return Array.from({ length: count }, (_, i) => i + 1);
}

/**
 * Return available degradation presets from a summary (fallback: ["clear", "heavy"]).
 */
export function availablePresets(summary: SubmissionSummary | null): string[] {
  if (!summary || Object.keys(summary.presets).length === 0) {
    return ["clear", "heavy"];
  }
  return Object.keys(summary.presets);
}
